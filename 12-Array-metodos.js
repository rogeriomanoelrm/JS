let linguagensDeProgramacao = []; 
// foi definido que isso recebera uma lista de dados


linguagensDeProgramacao.unshift("JavaScript",);
// metodo unshift incluira um novo item na posição zero


linguagensDeProgramacao.push("PHP");
// Método push incluira no final da array.


linguagensDeProgramacao.unshift("C#");

linguagensDeProgramacao.push("Python");

console.log(linguagensDeProgramacao.length);


linguagensDeProgramacao.pop()
// este metodo irá retirar o ultimo itm do array.

console.log(linguagensDeProgramacao);


console.log(linguagensDeProgramacao.length);

linguagensDeProgramacao.shift();
// metodo shift irá tirar o primeiro.

console.log(linguagensDeProgramacao);





